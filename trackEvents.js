// Event listeners
contract.on('ProposeTransaction', (signer, txIndex, to, value, data) => {
    console.log('ProposeTransaction Event:');
    console.log('Signer:', signer);
    console.log('Transaction Index:', txIndex);
    console.log('To:', to);
    console.log('Value:', value.toString());
    console.log('Data:', data);
    console.log('----------------------------------');
});

contract.on('ApproveTransaction', (signer, txIndex) => {
    console.log('ApproveTransaction Event:');
    console.log('Signer:', signer);
    console.log('Transaction Index:', txIndex);
    console.log('----------------------------------');
});

contract.on('ExecuteTransaction', (account, txIndex) => {
    console.log('ExecuteTransaction Event:');
    console.log('Account:', account);
    console.log('Transaction Index:', txIndex);
    console.log('----------------------------------');
});

contract.on('RevokeConfirmation', (signer, txIndex) => {
    console.log('RevokeConfirmation Event:');
    console.log('Signer:', signer);
    console.log('Transaction Index:', txIndex);
    console.log('----------------------------------');
});

